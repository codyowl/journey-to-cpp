#include <iostream>

using namespace std;

//array declaration 
//syntax datatype ARRAYNAME[ARRAYSIZE]

int marks[10];

//main function 
int main() {
    //assigning values to the array
    marks[0] = 35;
    marks[1] = 45;
    marks[2] = 55;
    marks[3] = 65;
    marks[4] = 75;
    marks[5] = 85;
    marks[6] = 95;
    marks[7] = 100;
    marks[8] = 120;
    marks[9] = 140;
    
    cout << "The first element in the array :" << marks[0] << endl;
    cout << "The second element in the array: " << marks[1] << endl;
    cout << "The third element in the array: " << marks[2] << endl;
    
    //complete element 
    cout << "All the elements in the array: " << marks[] << endl;
    
}
